#' implemented, one for the pre-processing of NetCDF and tif raster files as
#' made available from a increasing number of data-providing institutions around
#' the globe and a second one for the upscaling of physical station data by
#' interpolation methods. For processing all used datasets MUST have geographic
#' coordinates using WGS 84 as datum.
